import type { Lesson } from "../types/learning";
import { languages } from "./languages";

type SupplementalLessonSeed = {
  id: string;
  languageId: Lesson["languageId"];
  unitId: Lesson["unitId"];
  title: string;
  description: string;
  order: number;
  xpReward: number;
  estimatedMinutes: number;
  focusTerm: string;
  focusTranslation: string;
  phrase: string;
  phraseTranslation: string;
  teacherName: string;
};

function createSupplementalLesson({
  description,
  estimatedMinutes,
  focusTerm,
  focusTranslation,
  id,
  languageId,
  order,
  phrase,
  phraseTranslation,
  teacherName,
  title,
  unitId,
  xpReward,
}: SupplementalLessonSeed): Lesson {
  const vocabularyId = `${id}-vocab-focus`;
  const phraseId = `${id}-phrase-focus`;
  const languageName =
    languages.find((language) => language.id === languageId)?.name ??
    "language";

  return {
    id,
    languageId,
    unitId,
    title,
    description,
    level: "beginner",
    order,
    xpReward,
    estimatedMinutes,
    goals: [
      {
        id: `${id}-goal-1`,
        description: `Understand ${focusTerm} in a useful beginner phrase.`,
      },
      {
        id: `${id}-goal-2`,
        description: `Use ${phrase} in a short conversation.`,
      },
    ],
    vocabulary: [
      {
        id: vocabularyId,
        term: focusTerm,
        translation: focusTranslation,
        partOfSpeech: "expression",
        example: phrase,
      },
    ],
    phrases: [
      {
        id: phraseId,
        text: phrase,
        translation: phraseTranslation,
        context: description,
      },
    ],
    activities: [
      {
        id: `${id}-activity-1`,
        type: "vocabulary-card",
        prompt: "Learn this useful phrase.",
        vocabularyId,
      },
      {
        id: `${id}-activity-2`,
        type: "multiple-choice",
        prompt: `What does ${focusTerm} mean?`,
        options: [focusTranslation, "hello", "thank you"],
        correctOption: focusTranslation,
      },
      {
        id: `${id}-activity-3`,
        type: "translation",
        prompt: `Translate: ${phraseTranslation}.`,
        answer: phrase,
      },
    ],
    aiTeacherPrompt: {
      persona: `You are ${teacherName}, a warm, human, energetic ${languageName} teacher. Teach like a real-world tutor who is friendly, focused, and encouraging.`,
      teachingObjective: `Teach only this ${title.toLowerCase()} lesson for ${languageName}: ${description} Stay strictly within ${focusTerm}, ${phrase}, their translations, and this lesson context.`,
      conversationStarter: phrase,
      correctionStyle:
        "Listen to the learner first. If they are close, praise them and model the lesson phrase; if not, correct one small thing in English and invite one more try.",
      audioInstructions:
        `Mostly speak English. Introduce ${focusTerm} as "${focusTranslation}" and ${phrase} as "${phraseTranslation}" slowly, use contractions and gentle energy, ask for one response, then stop and wait.`,
    },
  };
}

